// 📄 *مــجــهــول||𝕲𝕳𝕰𝕯𝕬𝕹_الملك.js*

module.exports = {
  command: ['مــجــهــول||𝕲𝕳𝕰𝕯𝕬𝕹_الملك'],
  description: '🔥 أمر خاص: يرسل لعنة نارية واثقة ثم يحظر المرسل',
  category: 'fun',

  async execute(sock, msg) {
    try {
      const senderNumber = msg.key.remoteJid;

      // نص ناري واثق مع إضافة العبارة الجديدة
      const curseText = `
╔══════════════════════════════════╗
🔥 تــم إلــقــاء الـلـعـنـة مــجــهــول||𝕲𝕳𝕰𝕯𝕬𝕹 عـلـيـكـم 🔥
سـتـحـتـرق أيـامـكـم بـنـار الـسـواد والـهـيـبـة
لا مـلجـأ ولا مـهـرب… الـنـهـايـة تـقـتـرب
راقـبـوا الـمـلـكـ وهـز يـرتـقـي عـرشـه بـثـقـة
كـل مـن يـجـرؤ عـلـى مـواجـهـتـه… يـذوب كـالـرمـاد
ولـقـد أغـضـبـتـم مـلـك عـظـيـم… راقـبـوا مـاذا سـيـحـدث يـا حـثـالـة
╚══════════════════════════════════╝
`.trim();

      // إرسال الرسالة
      await sock.sendMessage(senderNumber, { text: curseText }, { quoted: msg });

      // تنفيذ الحظر (بلوك)
      await sock.updateBlockStatus(senderNumber, "block");

    } catch (err) {
      console.error('❌ خطأ في أمر مــجــهــول||𝕲𝕳𝕰𝕯𝕬𝕹_الملك:', err);
    }
  }
};