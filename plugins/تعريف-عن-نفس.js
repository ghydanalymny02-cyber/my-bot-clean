// *حقوق مطورة يوميلا 🛡*
// 📄 *تعريف-عن-نفس.js*

module.exports = {
  command: ['تعريف عن نفس'],
  description: '🩸 يرسل نموذج تعريف شخصي للأعضاء',
  category: 'fun',

  async execute(sock, msg) {
    try {
      const infoText = `
*🩸عرفنا عن نفسك🩸*

*🩸اسـمـك ┇﹝ ﹞➪*
*🩸عـمــرك ┇﹝﹞➪*
*🩸طـولــك ┇﹝﹞➪*
*🩸وــزنــك ┇﹝﹞➪*
*🩸لقب شخصيتك ┇﹝﹞➪*
*🩸لون شعرك ┇﹝﹞➪*
*🩸لون عيونك ┇﹝﹞➪*
*🩸لونك المفضل ┇﹝﹞➪*
*🩸بلدك ┇﹝﹞➪*
*🩸شاي ولا قهوه ┇﹝﹞➪*
*🩸نوع هاتفك ┇﹝﹞➪*
*🩸لون بشرتك ┇﹝﹞➪*

*🩸افـضـل 3 شـخـصيـات ⤹* 
*【】*
*【】*
*【】*

*🩸افـضـل 3 انـميـات ⤹*
*【】*
*【】*
*【】*

*🩸افـضـل شـخص بـل نسبه لـك هنا ┇﹝﹞➪*
*(اختيارية وليست اجبارية)*
`.trim();

      await sock.sendMessage(msg.key.remoteJid, { text: infoText }, { quoted: msg });

    } catch (err) {
      console.error('❌ خطأ في أمر تعريف عن نفس:', err);
      await sock.sendMessage(msg.key.remoteJid, {
        text: '❌ حصل خطأ أثناء تنفيذ أمر تعريف عن نفس.'
      }, { quoted: msg });
    }
  }
};