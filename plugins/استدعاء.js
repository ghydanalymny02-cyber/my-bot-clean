module.exports = {
  command: ['استدعاء'],
  category: 'انمي',
  description: 'طقوس استدعاء أي شخصية أنمي',
  usage: '.استدعاء [اسم]',  

  async execute(sock, msg, args) {
    try {
      let text = msg.message?.conversation || msg.message?.extendedTextMessage?.text || '';
      args = text.trim().split(/\s+/).slice(1); // تجاهل الأمر نفسه
      let name = args.join(' ').trim();

      const reply = msg.message?.extendedTextMessage?.contextInfo?.quotedMessage;
      if (!name && reply) {
        name = reply.sender ? reply.sender.split('@')[0] : '';
      }

      if (!name) {
        return sock.sendMessage(msg.key.remoteJid, {
          text: '👁️‍🗨️ اكتب اسم الشخصية بعد الأمر.\nمثال: `.استدعاء ناروتو`'
        });
      }

      const imagePath = path.join(characterDir, `${name}.jpg`);
      if (!fs.existsSync(imagePath)) {
        return sock.sendMessage(msg.key.remoteJid, {
          text: `❌ لم أتمكن من العثور على صورة للشخصية "${name}".`
        });
      }

      const buffer = fs.readFileSync(imagePath);
      const quote = characterQuotes[name] || '✨ الشخصية المختارة جاهزة!';

      await sock.sendMessage(msg.key.remoteJid, {
        image: buffer,
        caption: `🔮 *استدعاء ${name} تم التنفيذ..* \n\n*${quote}*
❄ 𝒀𝑼𝑴𝑰𝑳𝑨 𝑩𝒐𝒕꧂`
      });

    } catch (err) {
      console.error('🚨 خطأ في أمر استدعاء:', err);
      await sock.sendMessage(msg.key.remoteJid, {
        text: '❌ حدث خطأ أثناء تنفيذ الأمر.\n' + (err.message || 'تفاصيل غير معروفة.')
      });
    }
  }
};