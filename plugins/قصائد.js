  command: 'قصائد',
  category: 'تسلية',
  description: 'يُرْسِلُ صُورَةَ الطَّالِبِ مَدْمُوجَةً مَعَ قَصِيدَةٍ مَزَخْرَفَةٍ',

  async execute(sock, msg, args = []) {
    const chatId = msg.key.remoteJid;
    const sender = msg.key.participant || msg.participant || msg.key.remoteJid;

    // اختيار قصيدة عشوائية
    const poem = poems[Math.floor(Math.random() * poems.length)];

    // تنسيق القصيدة داخل التعليق
    const caption = `
╭───❖ 「 📝 ${poem.title} 」 ❖───╮
🎭 النَّوْعُ: ${poem.type}
🖋️ الشَّاعِرُ: ${poem.author}
╰───────────────╯

${poem.text}

❖═════⏣ تَوْقِيع مـــجـــهـــول ⏣═════❖
مـــجـــهـــول⊰𝑩𝑶𝑻 🌋`;

    // إرسال الصورة مدموجة مع القصيدة
    try {
      const profilePic = await sock.profilePictureUrl(sender, 'image');
      await sock.sendMessage(chatId, {
        image: { url: profilePic },
        caption: caption
      }, { quoted: msg });
    } catch {
      await sock.sendMessage(chatId, {
        text: '📸 لَا تُوجَدُ صُورَةٌ مُتَاحَةٌ لِهَذَا الْمُسْتَخْدِمِ.'
      }, { quoted: msg });
    }
  }
