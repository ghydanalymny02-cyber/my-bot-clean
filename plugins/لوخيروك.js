const { jidDecode } = require('@whiskeysockets/baileys');

// دالة لتحويل JID العادي
const decode = jid => (jidDecode(jid)?.user || jid.split('@')[0]) + '@s.whatsapp.net';

module.exports = {
    command: 'لوخيروك',
    description: 'يعطيك سؤال لو خيروك 🧠',
    usage: '.لوخيروك',
    category: 'fun',

    async execute(sock, msg) {
        try {
            const groupJid = msg.key.remoteJid;

            const questions = [
                "لو خيروك تعيش في الماضي ولا المستقبل؟",
                "لو خيروك تكون غني لكن وحيد، ولا فقير وعندك ناس تحبك؟",
                "لو خيروك تمسح ذكرياتك الحزينة ولا تحتفظ بيها وتتعلم منها؟",
                "لو خيروك تسافر كل العالم بس ما ترجع بلدك، ولا تبقى في بلدك طول حياتك؟",
                "لو خيروك تعيش بدون إنترنت ولا بدون أصدقاء؟",
                "لو خيروك تكون مشهور بس الكل يكرهك، ولا مجهول والناس تحبك؟",
                "لو خيروك تحكي الحقيقة دائماً ولا تكذب مجاملة؟",
                "لو خيروك تنسى أعز شخص في حياتك ولا هو ينساك؟",
                "لو خيروك تكون أذكى شخص في العالم ولا أجمل شخص؟",
                "لو خيروك تشتغل شغل ممل ويعطيك فلوس كثير، ولا شغل ممتع بس فلوسه قليلة؟",
                "لو خيروك تاكل أكلة وحدة طول حياتك، تختار ايه؟",
                "لو خيروك تعيش في جزيرة لوحدك، ولا في مدينة مزعجة مليانة ناس؟",
                "لو خيروك ما تتكلم أبداً ولا ما تسمع أبداً؟",
                "لو خيروك تعيش يوم كطفل مرة ثانية، ولا يوم كشخص عجوز حكيم؟",
                "لو خيروك تمحي ذنب ارتكبته، ولا تعيد لحظة سعيدة مرت؟",
                "لو خيروك تشوف المستقبل ولا ترجع للماضي وتغير شيء؟",
                "لو خيروك تكون ملك العالم ليوم واحد، تعمل ايه؟",
                "لو خيروك تضحي بنفسك عشان تنقذ اللي تحبهم، تعملها؟",
                "لو خيروك تفضل دايمًا صادق حتى لو جرحت الناس، ولا تكذب وتريحهم؟"
            ];

            const randomQuestion = questions[Math.floor(Math.random() * questions.length)];

            await sock.sendMessage(groupJid, {
                image: { url: 'https://files.catbox.moe/djpim5.jpg' },
                caption: `🤔 *لو خيروك:*\n\n${randomQuestion}`
            }, { quoted: msg });

        } catch (error) {
            console.error('❌ خطأ في أمر لو خيروك:', error);
            await sock.sendMessage(msg.key.remoteJid, {
                text: `❌ حصل خطأ:\n\n${error.message || error.toString()}`
            }, { quoted: msg });
        }
    }
};