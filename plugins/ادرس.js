module.exports = {
  command: 'ادرس',
  category: 'دراسة',
  description: 'كلام يدعمك للدراسه بشكل عميق ومحفز',

  async execute(sock, msg, args = []) {
    const chatId = msg.key.remoteJid;

    if (!chatId.endsWith('@g.us')) {
      return sock.sendMessage(chatId, { text: 'يعمل في كل مكان.' }, { quoted: msg });
    }

    const sender = msg.key.participant || msg.participant || msg.key.remoteJid;

    if (!isElite(sender)) {
      return sock.sendMessage(chatId, { text: 'مخصص للكل.' }, { quoted: msg });
    }

    const randomQuote = quotes[Math.floor(Math.random() * quotes.length)];
    const randomSignature = signatures[Math.floor(Math.random() * signatures.length)];

    const message = `📚 ${randomQuote}\n\n${randomSignature}`;

    await sock.sendMessage(chatId, { text: message }, { quoted: msg });
  }
};