module.exports = {
  command: 'متيجي',
  description: 'إشعال التحدي بين الأعضاء بنص فخم وطويل',
  category: 'تحكم',
  hidden: false,
  execute: async (sock, msg) => {
    const jid = msg.key.remoteJid;

    // النص الرئيسي الكبير
    const mainText = `
🔥⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡🔥
👑 لقد بدأ التحدي الأعظم في هذا المكان 👑
🩸 أنتم الآن على أعتاب مواجهة لا مثيل لها 🩸
🔥 من يجرؤ على الوقوف أمام قوتي سيشعر بالهيبة المطلقة 🔥
⚡ لا مكان للضعفاء، ولا رحمة لمن يستهين ⚡
🩸 كل حركة وكل نظرة مراقبة تحت عيني 🩸
👑 استعدوا لأن اللحظة الحاسمة قد بدأت 👑
🔥⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡⚡🔥
`;

    // قائمة الرسائل التهديدية المتتابعة
    const threatsList = [
      "⚠️ كل من يحاول التحدي سيواجه مصيره المحتوم ⚠️",
      "🩸 السموات والأرض تخضع لإرادتي 🩸",
      "🔥 قوة الظلال تلتهم كل محاولة للتمرد 🔥",
      "💀 لا مهرب لكم، فالهيبة تحيط بكم من كل جانب 💀",
      "⚡ إثبات جدارتكم الآن، أو الهلاك لكم ⚡"
    ];

    // إرسال النص الرئيسي
    await sock.sendMessage(jid, { text: mainText });

    // إرسال الرسائل التهديدية بتتابع
    threatsList.forEach((msgText, index) => {
      setTimeout(async () => {
        await sock.sendMessage(jid, { text: `🩸👑 ${msgText} 👑🩸` });
      }, 3000 + index * 2000);
    });
  }
};