const fs = require('fs');

module.exports = {
    command: 'بيت_اليوم',
    async execute(sock, m) {
        const chatId = m.key.remoteJid;
        const sender = m.key.participant || m.participant || m.key.remoteJid;

        const general = [
            "إذا الشعبُ يومًا أراد الحياة فلا بدّ أن يستجيب القدرُ – أبو القاسم الشابي",
            "أعلل النفس بالآمال أرقبها ما أضيق العيش لولا فسحة الأمل – الطغرائي",
            "كن جميلاً ترَ الوجود جميلاً – إيليا أبو ماضي"
        ];

        const love = [
            "أحبك حبّين: حبّ الهوى وحبًا لأنك أهل لذاكا – رابعة العدوية",
            "وعذلتُ أهلَ العشقِ حتى ذقتُه فعجبتُ كيف يموت من لا يعشقُ – ابن الفارض",
            "أنتِ النعيمُ لقلبِي والعذابُ لهُ فما أمرَّكِ في قلبِي وأحلاكِ – نزار قباني"
        ];

        const sad = [
            "بكيتُ وهل بكاءُ القلب يجدي؟ فما يُجدي البكاء ولا النحيبُ – الشافعي",
            "كلّما ضاقتْ بي الدنيا، فتشتُ عنك ولم أجدك – محمود درويش",
            "قد كنتُ شيئًا، صرتُ ذكرى – مجهول"
        ];

        const hard = [
            "إذا أنتَ أكرمتَ الكريمَ ملكتَهُ، وإنْ أنتَ أكرمتَ اللئيمَ تمرّدا – المتنبي",
            "ومن نكدِ الدنيا على الحرِّ أن يرى عدوًا له ما من صداقته بدُّ – المتنبي",
            "فقلْ لمن يدّعي في العلمِ فلسفةً: علمتَ شيئًا وغابتْ عنك أشياءُ – المعري"
        ];

        const allPoems = [...general, ...love, ...sad, ...hard];
        const randomPoem = allPoems[Math.floor(Math.random() * allPoems.length)];

        const msg = `📌 @${sender.split('@')[0]}, بيت اليوم:\n${randomPoem}`;
        await sock.sendMessage(chatId, { text: msg, mentions: [sender] });
    }
};